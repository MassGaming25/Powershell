Clear-Host

## version check
if($PSVersionTable.PSVersion.Major -ne 7){
    Write-Host "ERROR: You are using a old version of PowerShell! (Required: 7.x)" -ForegroundColor Red
    Pause
    Clear-Host
    Exit
}
Else{
    Write-Host "Version OK" -ForegroundColor Green
}

$tasks = @{}
While($true){
    While($true){
        ## Menu
        Clear-Host
        Write-Host "To do list" -ForegroundColor Cyan -BackgroundColor DarkCyan	
        ForEach($task in $tasks.GetEnumerator()){
            if($task.Value -eq $false){
                Write-Host "[ ]" $task.Name
            }
            Elseif($task.Value -eq $true){
                Write-Host "[" -NoNewline
                Write-Host "âœ”" -ForegroundColor Cyan -NoNewline
                Write-Host "]" $task.Name
            } 
    }
    Write-host ""
    Write-host "[1]: Add task [2]: Remove task [3]: Complete task [4]: Exit script" -ForegroundColor Cyan
    $command = Read-host -prompt "Please select your command."

    If($command -eq 1){
        Break
    }
    ElseIf($command -eq 2){
        Break
    }
    ElseIf($command -eq 3){
        Break
    }
    ElseIf($command -eq 4){
        Break
    }
    }


    ##Hash
    Switch($command){
        1{
            $TaskName = Read-Host -prompt "What is the name of the new task?"

            $TaskValue = $false

            $tasks.Add($TaskName, $TaskValue)
        }
        2{
            $Removetask = Read-host -prompt "What task do you want to remove?"

            $tasks.Remove($Removetask)
        }
        3{
            $TaskName1 = Read-Host -prompt "What task would you like to complete?"

            $tasks.Set_Item($TaskName1, $true)
        }
        4{
            $choice = Read-Host -prompt "Are you sure you want to shut down? all tasks will be removed upon exit. (y/n)"

            If($choice -eq "y"){
                Clear-Host
                Exit
            }
            Elseif($choice -eq "n"){
                Write-host "Cancelled"
            }
        }
        Default{
            Write-Host "ERROR: Please give a value!" -ForegroundColor Red
        }
    }

    Clear-Host
}


















































































































































































































































































































































































































































































































































































































































































































































































































































































































































